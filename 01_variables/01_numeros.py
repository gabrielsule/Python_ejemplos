#int
num = 42
print(num)
print(type(num), num)

#float
num = 42.0
print(num)
print(type(num), num)

num = 42/9
print(num)
print(type(num), num)

#round
num = round(42/9)
print(num)
print(type(num), num)

num = round(42/9, 2)
print(num)
print(type(num), num)

#conversion
num = int(42/9)
print(num)
print(type(num), num)